function getProduct(slug) {
  return PRODUCTS.find((p) => p.slug === slug) || PRODUCTS[0];
}
function getProject(slug) {
  return PROJECTS.find((p) => p.slug === slug) || PROJECTS[0];
}
function relatedProducts(slug, n = 3) {
  const cur = getProduct(slug);
  return PRODUCTS.filter((p) => p.slug !== slug && p.category === cur.category)
    .concat(PRODUCTS.filter((p) => p.slug !== slug && p.category !== cur.category))
    .slice(0, n);
}

